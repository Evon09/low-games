
var user = class user {

    userId = null;//Id do usuario
    nome = null;//Nome do usuario
    email = null;//Email do usuario
    senha = null;//Senha do usuario
    peso = null;//Peso da nota do Usuario


    constructor(id, nome, email, senha, nota) {

        this.userId = id;
        this.nome = nome;
        this.email = email;
        this.senha = senha;
        this.peso = nota;

    }




}




module.export = new user();